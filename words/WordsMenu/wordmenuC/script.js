function init() {
    selectedWords = words.slice();
    currentIndex = 0;
    translationDisplay.style.display = 'none';
    displayWord();
}

function removeDiacritics(str) {
    return str.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
}

function displayWord() {
    if (selectedWords.length === 0) {
        result.textContent = "Nie wybrano słówek do nauki!";
        return;
    }

    const currentWord = isEnglishToPolish ? selectedWords[currentIndex].english : selectedWords[currentIndex].polish;
    wordDisplay.textContent = currentWord;
    generateOptions(isEnglishToPolish ? selectedWords[currentIndex].polish : selectedWords[currentIndex].english);
    userInput.value = '';
    result.textContent = '';
    errorShown = false;
    speakWord();

    const correctTranslation = isEnglishToPolish ? selectedWords[currentIndex].polish : selectedWords[currentIndex].english;
    translationDisplay.textContent = correctTranslation;
}

function speakWord() {
    const currentWord = wordDisplay.textContent;
    const utterance = new SpeechSynthesisUtterance(currentWord);
    utterance.lang = isEnglishToPolish ? 'en-US' : 'pl-PL';
    window.speechSynthesis.speak(utterance);
}

function generateOptions(correctAnswer) {
    optionsContainer.innerHTML = '';
    const options = [correctAnswer];

    while (options.length < 4) {
        const randomWord = selectedWords[Math.floor(Math.random() * selectedWords.length)];
        const word = isEnglishToPolish ? randomWord.polish : randomWord.english;
        if (!options.includes(word)) options.push(word);
    }

    shuffle(options);

    options.forEach(option => {
        const btn = document.createElement('button');
        btn.textContent = option;
        btn.className = 'option-btn';
        btn.addEventListener('click', () => checkAnswer(option, correctAnswer));
        optionsContainer.appendChild(btn);
    });
}

function shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

function checkAnswer(selected, correct) {
    if (answerGiven) return;

    const userInputValue = removeDiacritics(userInput.value.trim().toLowerCase());
    const correctValue = removeDiacritics(correct.toLowerCase());
    const isCorrect = selected === correct || userInputValue === correctValue;

    if (isCorrect) {
        result.textContent = "Dobrze!";
        score++;
        answerGiven = true;
        errorShown = false;

        container.classList.add('bounce');
        highlightButtons("btn-yellow");

        setTimeout(() => {
            currentIndex = (currentIndex + 1) % selectedWords.length;
            answerGiven = false;
            displayWord();
        }, 600);

        setTimeout(() => {
            container.classList.remove('bounce');
            clearButtonHighlights();
        }, 600);
    } else {
        result.textContent = `Źle! Poprawne tłumaczenie to: ${correct}`;
        answerGiven = true;
        container.classList.add('shake');
        highlightButtons("btn-red");

        setTimeout(() => {
            container.classList.remove('shake');
            clearButtonHighlights();
        }, 600);
    }
}

function highlightButtons(className) {
    document.querySelectorAll('.option-btn').forEach(btn => btn.classList.add(className));
    toggleTranslationBtn.classList.add(className);
    switchLanguageBtn.classList.add(className);
    nextBtn.classList.add(className);
    submitBtn.classList.add(className);
    hideButtonx.classList.add(className);
    hideTitleButtonx.classList.add(className);
}

function clearButtonHighlights() {
    document.querySelectorAll('.option-btn').forEach(btn => {
        btn.classList.remove("btn-yellow", "btn-red");
    });
    toggleTranslationBtn.classList.remove("btn-yellow", "btn-red");
    switchLanguageBtn.classList.remove("btn-yellow", "btn-red");
    nextBtn.classList.remove("btn-yellow", "btn-red");
    submitBtn.classList.remove("btn-yellow", "btn-red");
    hideButtonx.classList.remove("btn-yellow", "btn-red");
    hideTitleButtonx.classList.remove("btn-yellow", "btn-red");
}

submitBtn.addEventListener('click', () => {
    const correctTranslation = isEnglishToPolish ? selectedWords[currentIndex].polish : selectedWords[currentIndex].english;
    checkAnswer(null, correctTranslation);
    errorShown = false;
});

nextBtn.addEventListener('click', () => {
    if (!answerGiven) return;
    currentIndex = (currentIndex + 1) % selectedWords.length;
    answerGiven = false;
    errorShown = false;
    displayWord();
});

document.addEventListener('keydown', function(event) {
    const now = Date.now();

    if (event.key === 'Enter') {
        event.preventDefault();

        if (now - lastEnterTime < 500) return;

        const hasText = userInput.value.trim() !== "";
        const anySelected = Array.from(document.querySelectorAll('.option-btn')).some(btn => btn.classList.contains('selected'));

        if (answerGiven) {
            nextBtn.click();
        } else if (!hasText && !anySelected) {
            if (!errorShown) {
                result.textContent = "Proszę wpisać tłumaczenie lub wybrać odpowiedź.";
                errorShown = true;
            } else {
                currentIndex = (currentIndex + 1) % selectedWords.length;
                answerGiven = false;
                errorShown = false;
                displayWord();
            }
        } else {
            submitBtn.click();
        }

        lastEnterTime = now;
    }

    if (!answerGiven && ['1', '2', '3', '4'].includes(event.key)) {
        event.preventDefault();
        const index = parseInt(event.key) - 1;
        const optionButtons = document.querySelectorAll('.option-btn');
        if (optionButtons[index]) {
            optionButtons[index].click();
        }
    }

    if (/^[a-zA-ZąćęłńóśźżĄĆĘŁŃÓŚŹŻ]$/.test(event.key)) {
        userInput.focus();
    }
});

